
public class 문제5 {
	public static void main(String[] arg) 
	{
	      int num1=10;   
	      System.out.printf("%d \n", num1);//10
	      num1++;
	      System.out.printf("%d \n", num1);//11
	      ++num1;
	      System.out.printf("%d \n", num1);//12
	      --num1;
	      System.out.printf("%d \n", num1);//11
	      num1--;
	      System.out.printf("%d \n", num1);//10
	}

}
